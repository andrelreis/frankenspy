"""
Set of constant used in this package in SI
"""
#: Permeability of free space [N A^{2}]
PERM_VACUUM = 4*3.14159265359*(10**-7)

#: Gravitational constant [m^3 kg^{-1} s^{-1}]
G = 6.67408*(10**-11)

#: Constant for transforming SI to mGAL
SI2MGAL = 10.**(5)

#: Constant for transforming SI to EOTVOS
SI2EOTVOS = 10.**(9)

#: Constant for transforming T to nT
T2NT = 10.**(9)

#: Constant proportional to Permeability of free space [H m^{-1}]
CM = 10.**(-7)

#: Mean radius of Earth [m]
MEAN_RADIUS_EARTH = 6378137.0
